const DB_NAME = 'evergreen-db';
const DB_VERSION = 2;
const PLANTS_STORE = 'plants';
const PENDING_STORE = 'pending_schedules';

let db;
const request = window.indexedDB.open(DB_NAME, DB_VERSION);

request.onerror = (event) => {
    console.error('IndexedDB error:', event.target.error);
};

request.onsuccess = (event) => {
    db = event.target.result;
    console.log('IndexedDB opened successfully');
};

request.onupgradeneeded = (event) => {
    const db = event.target.result;
    // plants store
    if (!db.objectStoreNames.contains(PLANTS_STORE)) {
        db.createObjectStore(PLANTS_STORE, { keyPath: 'id' });
        console.log('Plants object store created');
    }

    // pending schedules store. autoIncrements because offline entries have no server id yet
    if (!db.objectStoreNames.contains(PENDING_STORE)) {
        db.createObjectStore(PENDING_STORE, { autoIncrement: true });
        console.log('Pending schedules object store created');
    }
};

// save plants to IndexedDB
export function savePlants(plants) {
    const transaction = db.transaction([PLANTS_STORE], 'readwrite');
    const objectStore = transaction.objectStore(PLANTS_STORE);

    // clear old data first
    objectStore.clear();

    // add each plant
    plants.forEach(plant => {
        const req = objectStore.add(plant);
        req.onerror = (event) => {
            console.error('Error saving plant:', event.target.error);
        };
    });

    transaction.oncomplete = () => {
        console.log('All plants saved to IndexedDB');
    };
    transaction.onerror = (event) => {
        console.error('Transaction error:', event.target.error);
    };
}

// get all plants from indexedDB
export function getCachedPlants() {
    return new Promise((resolve, reject) => {
        const transaction = db.transaction([PLANTS_STORE], 'readonly');
        const objectStore = transaction.objectStore(PLANTS_STORE);
        const request = objectStore.getAll();

        request.onsuccess = (event) => {
            resolve(event.target.result);
        };
        request.onerror = (event) => {
            reject(event.target.error);
        };
    });
}

// save a schedule entry to IDB while offline
export function savePendingSchedule(scheduleData) {
    const transaction = db.transaction([PENDING_STORE], 'readwrite');
    const objectStore = transaction.objectStore(PENDING_STORE);

    const req = objectStore.add(scheduleData); // key is autogenerated

    req.onsuccess = () => {
        console.log('Schedule saved to IndexedDB as pending:', scheduleData);
    };
    req.onerror = (event) => {
        console.error('Error saving pending schedule:', event.target.error);
    };
}


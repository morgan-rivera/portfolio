// get all keys of pending schedules (needed to delete after sync)
export function getPendingScheduleKeys() {
    return new Promise((resolve, reject) => {
        const transaction = db.transaction([PENDING_STORE], 'readonly');
        const objectStore = transaction.objectStore(PENDING_STORE);
        const request = objectStore.getAllKeys();

        request.onsuccess = (event) => {
            resolve(event.target.result);
        };
        request.onerror = (event) => {
            reject(event.target.error);
        };
    });
}

// delete a pending schedule by its IDB key after it's been synced
export function deletePendingSchedule(key) {
    const transaction = db.transaction([PENDING_STORE], 'readwrite');
    const objectStore = transaction.objectStore(PENDING_STORE);

    const req = objectStore.delete(key);

    req.onsuccess = () => {
        console.log('Pending schedule removed from IndexedDB:', key);
    };
    req.onerror = (event) => {
        console.error('Error deleting pending schedule:', event.target.error);
    };
}

// save pending plant add
export function savePendingPlant(plantId) {
    const transaction = db.transaction([PENDING_STORE], 'readwrite');
    const objectStore = transaction.objectStore(PENDING_STORE);
    objectStore.add({ type: 'add_plant', plantId: plantId });
}

// get all pending actions
export function getPendingActions() {
    return new Promise((resolve, reject) => {
        const transaction = db.transaction([PENDING_STORE], 'readonly');
        const objectStore = transaction.objectStore(PENDING_STORE);
        const request = objectStore.getAll();
        request.onsuccess = (e) => resolve(e.target.result);
        request.onerror = (e) => reject(e.target.error);
    });
}